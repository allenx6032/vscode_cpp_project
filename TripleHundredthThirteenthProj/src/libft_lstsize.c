#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*lst1;

	if (lst != NULL)
	{
		i = 1;
		lst1 = lst;
		while (lst1->next != NULL)
		{
			lst1 = lst1->next;
			i++;
		}
		return (i);
	}
	else
		return (0);
}
