#include "libft.h"

int	puts_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
	{
		i += puts_fd("(null)", fd);
		return (i);
	}
	while (s[i])
		i += putc_fd(s[i], fd);
	return (i);
}
