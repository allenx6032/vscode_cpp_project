#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*lst1;

	if (lst != NULL)
	{
		lst1 = lst;
		while (lst1->next != NULL)
		{
			lst1 = lst1->next;
		}
		return (lst1);
	}
	else
		return (0);
}
