#include "libft.h"

void	ft_mint_free(int n, ...)
{
	int		i;
	va_list	args;

	i = 0;
	va_start(args, n);
	while (i < n)
	{
		free(va_arg(args, void *));
		i++;
	}
	va_end(args);
}
