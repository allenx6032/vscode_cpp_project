#include "libft.h"

void	ft_arr2dfree(void **arr)
{
	int	arr_len;
	int	i;

	arr_len = ft_arr2dlen((void **)arr);
	i = 0;
	while (i < arr_len)
	{
		free(arr[i]);
		i++;
	}
	if (arr)
		free(arr);
}
